package com.lec.java.for02;

public class For02Main {

	public static void main(String[] args) {
		System.out.println("For문 - 구구단 출력");
		
		//구구단 만들기
		for (int a = 1; a < 10; a++) {
			System.out.println("2 x "+ a +" = "+ (2 * a));
		}

		for (int i = 1; i <= 9; i++ ) {
			System.out.println("4 x "+i+" = "+(4*i));
		}
		
		
		
		
	} // end main()

} // end class For02Main












